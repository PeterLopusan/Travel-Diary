data class CallNumber(
    val root: String?,
    val suffixes: MutableList<String>?
) {
    fun getCallNumbers(): MutableList<String> {
        val list = mutableListOf<String>()
        suffixes?.forEach {
            list.add(root+it)
        }

        return list
    }
}

